-- Рассмотрим 2 выражения и редукцию до нормальной формы для них:
-- ((\x (\y x)) y) 5 -> (\y y) 5 -> 5
-- ((\x (\t x)) y) 5 -> (\t y) 5 -> y
-- Исходные выражения эквивалентны, а нормальные формы -- нет. 
-- Вывод: надо стараться не запутаться в переменных. В первом выражении у нас при редукции стала связанной
-- переменная y, что некорректно.
-- Можно исправить формулировку правила так:
-- Выражение (\x -> E) a можно без изменения смысла выражения заменить на Е,
-- в котором все вхождения x заменены на a, если E не содержит связанными переменные, входящие свободно в a.
-- То есть если все свободные переменные из начального выражения остаются свободными.
-- (А в случае совпадения имён переменных можно использовать альфа-конверсию.)
